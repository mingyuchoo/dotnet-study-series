// A literal is literally a hard-coded value
// Literal values are values that are constant and
// unchanged throughout the execution of the program

Console.WriteLine("Hello, World!"); // string literal
Console.WriteLine('a');             // character literal
Console.WriteLine(123);             // integer literal
Console.WriteLine(1.23F);           // floating-point literal (float)
Console.WriteLine(1.23);            // floating-point literal (double)
Console.WriteLine(1.23M);           // floating-point literal (decimal)
Console.WriteLine(true);            // boolean literal
Console.WriteLine(false);           // boolean literal
